import { describe, it, expect, beforeEach } from 'vitest';
import { emit, type EmitOptions } from '../emitter.js';
import {
	makeDescMessage,
	makeScalarField,
	makeMessageField,
	makeDescEnum,
	makeDescOneof,
	resetFieldCounter,
	ScalarType,
} from './test-factories.js';
import type { ProtoZodConfig } from '../types.js';

function makeEmitOptions(overrides: Partial<EmitOptions> = {}): EmitOptions {
	return {
		sortedMessages: [],
		enums: new Map(),
		config: {},
		zodImport: 'zod',
		...overrides,
	};
}

describe('emit', () => {
	beforeEach(() => {
		resetFieldCounter();
	});

	// ─── Structure ───────────────────────────────────────────────────────

	describe('file structure', () => {
		it('emits header with DO NOT EDIT comment', () => {
			const output = emit(makeEmitOptions());
			expect(output).toContain('AUTO-GENERATED by proto-to-zod');
			expect(output).toContain('DO NOT EDIT');
		});

		it('emits z import from configured source', () => {
			const output = emit(
				makeEmitOptions({ zodImport: '@kbve/astro/zod' }),
			);
			expect(output).toContain("import { z } from '@kbve/astro/zod';");
		});

		it('includes source paths in header', () => {
			const output = emit(
				makeEmitOptions({
					protoSource: '../proto/desc.binpb',
					configSource: '../proto/config.json',
				}),
			);
			expect(output).toContain('Source: ../proto/desc.binpb');
			expect(output).toContain('Config: ../proto/config.json');
		});
	});

	// ─── Preamble ────────────────────────────────────────────────────────

	describe('preamble', () => {
		it('emits preamble blocks before schemas', () => {
			const output = emit(
				makeEmitOptions({
					config: {
						preamble: [
							'const CUSTOM_RE = /^[a-z]+$/;',
							'type CustomType = string;',
						],
					},
				}),
			);
			const zImportIdx = output.indexOf('import { z }');
			const preambleIdx = output.indexOf('const CUSTOM_RE');
			expect(preambleIdx).toBeGreaterThan(zImportIdx);
		});

		it('handles empty/no preamble gracefully', () => {
			const output = emit(makeEmitOptions({ config: {} }));
			// Should still produce valid output
			expect(output).toContain('import { z }');
		});
	});

	// ─── Const arrays ────────────────────────────────────────────────────

	describe('const arrays', () => {
		it('emits const array from enum', () => {
			const enumDesc = makeDescEnum('pkg.Status', [
				{ name: 'STATUS_UNSPECIFIED', number: 0 },
				{ name: 'STATUS_ACTIVE', number: 1 },
			]);
			const output = emit(
				makeEmitOptions({
					enums: new Map([['pkg.Status', enumDesc]]),
					config: {
						enums: {
							'pkg.Status': {
								mode: 'const_array',
								stripPrefix: 'STATUS_',
							},
						},
						constArrays: [
							{
								name: 'Statuses',
								sourceEnum: 'pkg.Status',
								zodSchemaName: 'StatusSchema',
							},
						],
					},
				}),
			);
			expect(output).toContain('export const Statuses = [');
			expect(output).toContain(
				'export const StatusSchema = z.enum(Statuses);',
			);
		});

		it('skips const array when sourceEnum not found in enums map', () => {
			const output = emit(
				makeEmitOptions({
					config: {
						constArrays: [
							{
								name: 'Missing',
								sourceEnum: 'pkg.NotThere',
							},
						],
					},
				}),
			);
			expect(output).not.toContain('Missing');
		});
	});

	// ─── Schema emission ─────────────────────────────────────────────────

	describe('schema emission', () => {
		it('emits z.object with fields', () => {
			const msg = makeDescMessage('pkg.Item', [
				makeScalarField('id', ScalarType.INT32),
				makeScalarField('name', ScalarType.STRING),
			]);
			const output = emit(makeEmitOptions({ sortedMessages: [msg] }));
			expect(output).toContain('export const ItemSchema = z');
			expect(output).toContain('id: z.number(),');
			expect(output).toContain('name: z.string(),');
			expect(output).toContain(
				'export type Item = z.infer<typeof ItemSchema>;',
			);
		});

		it('respects topological order', () => {
			const inner = makeDescMessage('pkg.Inner', [
				makeScalarField('val', ScalarType.INT32),
			]);
			const outer = makeDescMessage('pkg.Outer', [
				makeMessageField('child', inner),
			]);
			const output = emit(
				makeEmitOptions({
					sortedMessages: [inner, outer],
				}),
			);
			const innerIdx = output.indexOf('InnerSchema');
			const outerIdx = output.indexOf('OuterSchema');
			expect(innerIdx).toBeLessThan(outerIdx);
		});

		it('adds .passthrough() for passthrough messages', () => {
			const msg = makeDescMessage('pkg.Flexible', [
				makeScalarField('id', ScalarType.INT32),
			]);
			const output = emit(
				makeEmitOptions({
					sortedMessages: [msg],
					config: { passthrough: ['pkg.Flexible'] },
				}),
			);
			expect(output).toContain('.passthrough();');
		});

		it('emits extra fields from config', () => {
			const msg = makeDescMessage('pkg.Extended', [
				makeScalarField('id', ScalarType.INT32),
			]);
			const output = emit(
				makeEmitOptions({
					sortedMessages: [msg],
					config: {
						extraFields: {
							'pkg.Extended': [
								{
									name: 'slug',
									zodExpr: 'z.string().optional()',
									comment: 'MDX only',
								},
							],
						},
					},
				}),
			);
			expect(output).toContain(
				'slug: z.string().optional(), // MDX only',
			);
		});

		it('applies field renames', () => {
			const msg = makeDescMessage('pkg.Item', [
				makeScalarField('item_name', ScalarType.STRING),
			]);
			const output = emit(
				makeEmitOptions({
					sortedMessages: [msg],
					config: {
						renames: { 'pkg.Item.item_name': 'name' },
					},
				}),
			);
			expect(output).toContain('name: z.string(),');
			expect(output).not.toContain('item_name:');
		});

		it('uses custom schema and type names', () => {
			const msg = makeDescMessage('pkg.Item', [
				makeScalarField('id', ScalarType.INT32),
			]);
			const output = emit(
				makeEmitOptions({
					sortedMessages: [msg],
					config: {
						schemaNames: { 'pkg.Item': 'MyItemSchema' },
						typeNames: { 'pkg.Item': 'MyItem' },
					},
				}),
			);
			expect(output).toContain('export const MyItemSchema = z');
			expect(output).toContain(
				'export type MyItem = z.infer<typeof MyItemSchema>;',
			);
		});
	});

	// ─── Oneof emission ─────────────────────────────────────────────────

	describe('oneof emission', () => {
		it('emits oneof as a union field in the schema', () => {
			const msgA = makeDescMessage('pkg.A', [
				makeScalarField('id', ScalarType.INT32),
			]);
			const msgB = makeDescMessage('pkg.B', [
				makeScalarField('id', ScalarType.INT32),
			]);
			const oneofFields = [
				makeMessageField('a_val', msgA),
				makeMessageField('b_val', msgB),
			];
			const oneof = makeDescOneof('choice', oneofFields);
			const msg = makeDescMessage(
				'pkg.Container',
				[makeScalarField('id', ScalarType.INT32), ...oneofFields],
				{ oneofs: [oneof] },
			);

			const output = emit(makeEmitOptions({ sortedMessages: [msg] }));
			// Should contain the oneof field named 'choice'
			expect(output).toContain('choice:');
			// Should contain z.union
			expect(output).toContain('z.union(');
			// Should NOT emit the individual oneof member fields separately
			const lines = output.split('\n');
			const fieldLines = lines.filter(
				(l) => l.includes('a_val:') || l.includes('b_val:'),
			);
			// a_val and b_val should only appear inside the union, not as standalone fields
			expect(
				fieldLines.every(
					(l) => l.includes('z.union') || l.includes('z.object'),
				),
			).toBe(true);
		});

		it('does not duplicate oneof member fields as top-level fields', () => {
			const oneofFields = [
				makeScalarField('str_val', ScalarType.STRING),
				makeScalarField('int_val', ScalarType.INT32),
			];
			const oneof = makeDescOneof('payload', oneofFields);
			const msg = makeDescMessage(
				'pkg.Wrapper',
				[makeScalarField('id', ScalarType.INT32), ...oneofFields],
				{ oneofs: [oneof] },
			);

			const output = emit(makeEmitOptions({ sortedMessages: [msg] }));
			// 'id' should appear as a standalone field
			expect(output).toContain('id: z.number(),');
			// 'payload' should appear as the oneof union
			expect(output).toContain('payload:');
			// str_val and int_val should NOT appear as standalone fields
			const lines = output.split('\n');
			const standaloneStr = lines.filter(
				(l) =>
					l.trim().startsWith('str_val:') && !l.includes('z.object'),
			);
			expect(standaloneStr).toHaveLength(0);
		});
	});

	// ─── Postamble ───────────────────────────────────────────────────────

	describe('postamble', () => {
		it('emits postamble code after schemas', () => {
			const msg = makeDescMessage('pkg.Foo', [
				makeScalarField('id', ScalarType.INT32),
			]);
			const output = emit(
				makeEmitOptions({
					sortedMessages: [msg],
					config: {
						postamble: [
							{
								name: 'SpecialSchema',
								code: 'export const SpecialSchema = z.lazy(() => FooSchema);',
							},
						],
					},
				}),
			);
			const schemaIdx = output.indexOf('FooSchema = z');
			const postIdx = output.indexOf('SpecialSchema = z.lazy');
			expect(postIdx).toBeGreaterThan(schemaIdx);
		});
	});

	// ─── Type guards & helpers ───────────────────────────────────────────

	describe('type guards and helpers', () => {
		it('emits type guard functions', () => {
			const output = emit(
				makeEmitOptions({
					config: {
						typeGuards: [
							{
								name: 'isWeapon',
								inputType: 'Item',
								returnType: 'item is Weapon',
								isTypePredicate: true,
								body: "return item.type === 'weapon';",
							},
						],
					},
				}),
			);
			expect(output).toContain('export function isWeapon(');
			expect(output).toContain('item: Item,');
			expect(output).toContain('): item is Weapon {');
			expect(output).toContain("return item.type === 'weapon';");
		});

		it('emits helper functions', () => {
			const output = emit(
				makeEmitOptions({
					config: {
						helpers: [
							{
								name: 'formatName',
								code: 'export function formatName(s: string): string { return s.trim(); }',
							},
						],
					},
				}),
			);
			expect(output).toContain('export function formatName');
		});
	});

	// ─── Rename conflict detection ──────────────────────────────────────

	describe('rename conflict detection', () => {
		it('throws when two fields map to the same output name', () => {
			const msg = makeDescMessage('pkg.Item', [
				makeScalarField('item_name', ScalarType.STRING),
				makeScalarField('name', ScalarType.STRING),
			]);
			expect(() =>
				emit(
					makeEmitOptions({
						sortedMessages: [msg],
						config: {
							renames: { 'pkg.Item.item_name': 'name' },
						},
					}),
				),
			).toThrow(/Rename conflict.*pkg\.Item.*"name"/);
		});
	});
});
