-- gh.requeue_audit — denormalized so audit survives gh.service_purge_old_delivered
-- sweeps. issue_event_id is recorded as a historical pointer only (no FK).

CREATE TABLE IF NOT EXISTS gh.requeue_audit (
    id                BIGINT       GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    issue_event_id    BIGINT       NOT NULL,
    repo_key          TEXT         NOT NULL,
    owner             TEXT         NOT NULL,
    repo              TEXT         NOT NULL,
    number            INT          NOT NULL,
    event_type        TEXT         NOT NULL,
    repos             TEXT[]       NOT NULL,
    reason            TEXT,
    old_state         SMALLINT     NOT NULL,
    old_attempts      INT          NOT NULL,
    old_claim_token   UUID,
    old_claimed_at    TIMESTAMPTZ,
    old_delivered_at  TIMESTAMPTZ,
    old_last_error    TEXT,
    forced            BOOLEAN      NOT NULL DEFAULT FALSE,
    created_at        TIMESTAMPTZ  NOT NULL DEFAULT now(),
    CONSTRAINT gh_requeue_audit_reason_len_chk
        CHECK (reason IS NULL OR char_length(reason) <= 512),
    CONSTRAINT gh_requeue_audit_old_state_chk
        CHECK (old_state IN (0, 1, 2, 3)),
    CONSTRAINT gh_requeue_audit_old_attempts_chk
        CHECK (old_attempts >= 0),
    CONSTRAINT gh_requeue_audit_old_last_error_len_chk
        CHECK (old_last_error IS NULL OR char_length(old_last_error) <= 2048)
);

COMMENT ON TABLE gh.requeue_audit IS
    'Append-only audit trail of manual gh.issue_event requeues triggered by the dashboard / ops tooling. Denormalized: gh.issue_event rows can be purged by retention sweeps; audit history persists. service_role only.';

