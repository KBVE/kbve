CREATE OR REPLACE FUNCTION gh.service_get_recent_pending_events(
    p_repos TEXT[],
    p_limit INT DEFAULT 10
)
RETURNS TABLE (
    id                BIGINT,
    owner             TEXT,
    repo              TEXT,
    number            INT,
    event_type        TEXT,
    actor             TEXT,
    delivery_state    SMALLINT,
    delivery_attempts INT,
    created_at        TIMESTAMPTZ,
    claimed_at        TIMESTAMPTZ
)
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
