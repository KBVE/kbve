#!/usr/bin/env node
/**
 * Generate TypeScript types from packages/data/openapi/openapi.json.
 *
 * Reads the spec emitted by `axum-kbve --emit-openapi`, runs
 * `openapi-typescript` over it, and writes a typed `paths` + `components`
 * surface into the astro-kbve schema dir so the frontend can consume
 * fully-typed fetch wrappers.
 *
 * Usage:
 *   npx tsx packages/data/codegen/gen-openapi-types.mjs
 */

import { execSync } from 'node:child_process';
import { existsSync, mkdirSync, writeFileSync, readFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '../../..');
const specPath = resolve(repoRoot, 'packages/data/openapi/openapi.json');
const outDir = resolve(
	repoRoot,
	'apps/kbve/astro-kbve/src/data/schema/openapi',
);
const outTypes = resolve(outDir, 'types.ts');
const outIndex = resolve(outDir, 'index.ts');

if (!existsSync(specPath)) {
	console.error(
		`✗ Missing ${specPath}\n  Run \`npx nx run axum-kbve:emit-openapi\` first.`,
	);
	process.exit(1);
}

mkdirSync(outDir, { recursive: true });

console.log('=== OpenAPI → TypeScript types ===\n');
console.log(`  spec:   ${specPath}`);
console.log(`  output: ${outTypes}`);

// openapi-typescript via npx — no devDep to manage. First run pulls the
// package; subsequent runs hit the npx cache.
//
// --root-types emits the path object + components at the top of the file
// so consumers can `import type { paths, components } from '@/data/schema/openapi'`
// without dotting into a default-export blob.
const banner = [
	'/**',
	' * Generated by packages/data/codegen/gen-openapi-types.mjs',
	' * DO NOT EDIT — regenerate via:',
	' *   npx tsx packages/data/codegen/gen-openapi-types.mjs',
	' *',
	' * Source: packages/data/openapi/openapi.json (emit via',
	' *   `npx nx run axum-kbve:emit-openapi`)',
	' */',
].join('\n');

try {
	const generated = execSync(
		`npx --yes openapi-typescript@7 "${specPath}"`,
		{ cwd: repoRoot, encoding: 'utf8' },
	);

	const final = `${banner}\n\n${generated.trimStart()}`;
	writeFileSync(outTypes, final);
	console.log('  ✓ types.ts');
} catch (err) {
	console.error('✗ openapi-typescript failed:');
	console.error(err.stderr?.toString() || err.message);
	process.exit(1);
}

// Barrel export so `import type { paths } from '@/data/schema/openapi'` works.
const indexBody = [
	'/**',
	' * Generated barrel for the OpenAPI types. See ./types.ts for the',
	' * spec-derived path + component definitions.',
	' */',
	"export type * from './types';",
	'',
].join('\n');
writeFileSync(outIndex, indexBody);
console.log('  ✓ index.ts');

// Quick sanity check — the spec had at least one path; the generated types
// file should reference `paths` + `components` somewhere.
const wrote = readFileSync(outTypes, 'utf8');
if (!wrote.includes('paths') || !wrote.includes('components')) {
	console.warn(
		'⚠ Generated types.ts did not include `paths`/`components`. ' +
			'Check that the spec at packages/data/openapi/openapi.json is non-empty.',
	);
	process.exit(2);
}

console.log('\n=== Done ===');
