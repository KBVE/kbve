#!/usr/bin/env node
/**
 * Generate Rareicon-side mapdb artifacts from the shared mdx pool.
 *
 * Reads: apps/kbve/astro-kbve/src/content/docs/mapdb/*.mdx
 * Writes:
 *   1. apps/rareicon/unity-rareicon/Assets/_RareIcon/Scripts/Data/MapdbRefs.cs
 *      — FNV-1a 32-bit hashes of each mdx `ref` as C# const uints.
 *      Burst-friendly lookup keys; Unity code reads registry.ByRef[MapdbRefs.Farm].
 *   2. apps/rareicon/unity-rareicon/Assets/StreamingAssets/mapdb.json
 *      — Filtered + flattened JSON Rareicon consumes at runtime.
 *
 * Runs via Nx: `npx nx run rareicon:sync-mapdb`.
 *
 * Standalone — does not depend on the Astro build.
 */

import { readdirSync, readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { resolve, dirname, basename } from 'node:path';
import { fileURLToPath } from 'node:url';
import matter from 'gray-matter';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '../../..');

const MDX_DIR = resolve(REPO_ROOT, 'apps/kbve/astro-kbve/src/content/docs/mapdb');
const OUT_CS = resolve(
	REPO_ROOT,
	'apps/rareicon/unity-rareicon/Assets/_RareIcon/Scripts/Data/MapdbRefs.cs',
);
const OUT_JSON = resolve(
	REPO_ROOT,
	'apps/rareicon/unity-rareicon/Assets/StreamingAssets/mapdb.json',
);

// Which proto WorldObjectType values Rareicon actually consumes. Skips shrine
// (no mechanic) + any future types we haven't shipped. See apps/rareicon/MAPDB.md.
const RAREICON_TYPES = new Set([
	'building',
	'resource_node',
	'settlement',
	'npc_marker',
	'landmark',
	'arena',
]);

// FNV-1a 32-bit. Deterministic, zero deps, Burst-compat lookup key.
function fnv1a32(str) {
	let hash = 0x811c9dc5 >>> 0;
	for (let i = 0; i < str.length; i++) {
		hash ^= str.charCodeAt(i);
		hash = Math.imul(hash, 0x01000193) >>> 0;
	}
	return hash;
}

// "dusty-bazaar" → "DustyBazaar" for C# identifier
function refToPascal(ref) {
	return ref
		.split(/[-_]/)
		.map((p) => (p.length > 0 ? p[0].toUpperCase() + p.slice(1) : ''))
		.join('');
}

function loadAllMdx() {
	const entries = readdirSync(MDX_DIR)
		.filter((f) => f.endsWith('.mdx'))
		.sort();

	const records = [];
	for (const file of entries) {
		const full = resolve(MDX_DIR, file);
		const raw = readFileSync(full, 'utf8');
		const parsed = matter(raw);
		const fm = parsed.data;

		if (!fm || typeof fm !== 'object') continue;
		if (!fm.ref || !fm.type) {
			console.warn(`⚠ ${file}: missing ref/type, skipping`);
			continue;
		}
		if (!RAREICON_TYPES.has(fm.type)) continue;
		if (fm.drafted === true) continue;

		records.push({ file, frontmatter: fm });
	}
	return records;
}

function emitRefsCs(records) {
	const lines = [];
	lines.push('// <auto-generated/>');
	lines.push('// Generated by packages/data/codegen/gen-rareicon-mapdb.mjs');
	lines.push('// Source: apps/kbve/astro-kbve/src/content/docs/mapdb/*.mdx');
	lines.push('// Regenerate via: npx nx run astro-kbve:sync:rareicon-mapdb');
	lines.push('// Do not edit by hand.');
	lines.push('');
	lines.push('namespace RareIcon');
	lines.push('{');
	lines.push('    /// <summary>FNV-1a 32-bit hashes of each mdx entry\'s `ref` slug.');
	lines.push('    /// Runtime keys for MapdbSingleton + BuildingPrefabRegistry lookups. Burst-friendly (uint, zero allocations).</summary>');
	lines.push('    public static class MapdbRefs');
	lines.push('    {');

	const byType = new Map();
	for (const { frontmatter } of records) {
		const t = frontmatter.type;
		if (!byType.has(t)) byType.set(t, []);
		byType.get(t).push(frontmatter);
	}

	for (const [type, list] of byType.entries()) {
		lines.push('');
		lines.push(`        // ${type} (${list.length})`);
		for (const fm of list) {
			const name = refToPascal(fm.ref);
			const hash = fnv1a32(fm.ref).toString(16).toUpperCase().padStart(8, '0');
			lines.push(`        public const uint ${name.padEnd(24)} = 0x${hash}u;  // ${fm.ref}`);
		}
	}

	lines.push('    }');
	lines.push('}');
	lines.push('');
	return lines.join('\n');
}

function emitJson(records) {
	// Flatten to a minimal shape Unity can deserialize via Newtonsoft.
	// Keeps the full WorldObjectDef frontmatter; drops mdx-only fields.
	const entries = records.map(({ frontmatter }) => {
		// strip Astro rendering fields — Rareicon uses its own shader / pixel grid
		const {
			pixels_per_unit, pivot, pivot_x, pivot_y, mesh_type, extrude_edges,
			sorting_layer, sorting_index, static_sorting, wrap_mode, animation,
			title,  // duplicates `name`
			...gameData
		} = frontmatter;
		return gameData;
	});

	const bundle = {
		version: 1,
		generated_at: new Date().toISOString(),
		count: entries.length,
		entries,
	};

	return JSON.stringify(bundle, null, 2) + '\n';
}

function main() {
	console.log(`Reading mdx from ${MDX_DIR}`);
	const records = loadAllMdx();
	console.log(`Loaded ${records.length} Rareicon-relevant entries:`);

	const countByType = {};
	for (const { frontmatter } of records) {
		countByType[frontmatter.type] = (countByType[frontmatter.type] || 0) + 1;
	}
	for (const [type, n] of Object.entries(countByType).sort()) {
		console.log(`  ${type.padEnd(15)} ${n}`);
	}

	// Ensure output dirs exist
	for (const p of [OUT_CS, OUT_JSON]) {
		const d = dirname(p);
		if (!existsSync(d)) mkdirSync(d, { recursive: true });
	}

	writeFileSync(OUT_CS, emitRefsCs(records));
	console.log(`✓ Wrote ${OUT_CS}`);

	writeFileSync(OUT_JSON, emitJson(records));
	console.log(`✓ Wrote ${OUT_JSON}`);
}

main();
