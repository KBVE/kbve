#!/usr/bin/env node
import {
	readdirSync,
	readFileSync,
	writeFileSync,
	mkdirSync,
	existsSync,
} from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { PNG } from 'pngjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '../../..');

const SRC_ROOT = resolve(
	repoRoot,
	'apps/rareicon/unity-rareicon/Assets/_RareIcon/Textures/Environment',
);
const ATLAS_OUT = resolve(
	repoRoot,
	'apps/rareicon/unity-rareicon/Assets/StreamingAssets/hextile-atlas.png',
);
const SOURCE_OUT = resolve(
	repoRoot,
	'apps/rareicon/unity-rareicon/Assets/_RareIcon/Scripts/ECS/Hex/HexTileAtlas.Generated.cs',
);

const TILE_W = 64;
const TILE_H = 84;
const COLS = 5;

const CATEGORY_ORDER = ['tilemap', 'props'];

function ensureDir(p) {
	if (!existsSync(p)) mkdirSync(p, { recursive: true });
}

function toIdentifier(name) {
	const base = name.replace(/\.png$/i, '');
	const parts = base.split(/[^A-Za-z0-9]+/).filter(Boolean);
	return parts.map((p) => p.charAt(0).toUpperCase() + p.slice(1)).join('');
}

function collectTiles() {
	const tiles = [];
	for (const cat of CATEGORY_ORDER) {
		const dir = resolve(SRC_ROOT, cat);
		if (!existsSync(dir)) continue;
		const files = readdirSync(dir)
			.filter((f) => f.toLowerCase().endsWith('.png'))
			.sort();
		for (const file of files) {
			tiles.push({
				category: cat,
				file,
				abs: resolve(dir, file),
				ident: toIdentifier(file),
			});
		}
	}
	return tiles;
}

function loadTile(abs) {
	const png = PNG.sync.read(readFileSync(abs));
	if (png.width !== TILE_W || png.height !== TILE_H) {
		throw new Error(
			`${abs} is ${png.width}x${png.height}, expected ${TILE_W}x${TILE_H}`,
		);
	}
	return png.data;
}

function placeTile(atlas, atlasW, tile, slot) {
	const sx = (slot % COLS) * TILE_W;
	const sy = Math.floor(slot / COLS) * TILE_H;
	for (let y = 0; y < TILE_H; y++) {
		const srcOff = y * TILE_W * 4;
		const dstOff = ((sy + y) * atlasW + sx) * 4;
		tile.copy(atlas, dstOff, srcOff, srcOff + TILE_W * 4);
	}
}

function main() {
	const tiles = collectTiles();
	if (tiles.length === 0) {
		console.error(`No PNG tiles found under ${SRC_ROOT}`);
		process.exit(1);
	}

	const rows = Math.ceil(tiles.length / COLS);
	const atlasW = COLS * TILE_W;
	const atlasH = rows * TILE_H;
	const atlas = Buffer.alloc(atlasW * atlasH * 4);

	tiles.forEach((t, slot) => placeTile(atlas, atlasW, loadTile(t.abs), slot));

	const png = new PNG({ width: atlasW, height: atlasH });
	atlas.copy(png.data);
	const encoded = PNG.sync.write(png, { deflateLevel: 9, deflateStrategy: 3 });
	ensureDir(dirname(ATLAS_OUT));
	writeFileSync(ATLAS_OUT, encoded);
	console.log(`Wrote ${ATLAS_OUT} (${atlasW}x${atlasH}, ${tiles.length} tiles)`);

	const uNorm = TILE_W / atlasW;
	const vNorm = TILE_H / atlasH;

	const consts = tiles
		.map((t, id) => `        public const int ${t.ident} = ${id};`)
		.join('\n');

	const lines = [
		'// <auto-generated/>',
		'// Source: packages/data/codegen/gen-hextile-atlas.mjs',
		'// Regenerate: npx nx run astro-kbve:sync:hextile-atlas',
		'',
		'using Unity.Mathematics;',
		'',
		'namespace RareIcon',
		'{',
		'    public static class HexTileAtlas',
		'    {',
		`        public const int   AtlasWidth  = ${atlasW};`,
		`        public const int   AtlasHeight = ${atlasH};`,
		`        public const int   TileWidth   = ${TILE_W};`,
		`        public const int   TileHeight  = ${TILE_H};`,
		`        public const int   Columns     = ${COLS};`,
		`        public const int   TileCount   = ${tiles.length};`,
		`        public const float TileU       = ${uNorm.toFixed(8)}f;`,
		`        public const float TileV       = ${vNorm.toFixed(8)}f;`,
		'',
		consts,
		'',
		'        public static float4 GetUV(int id)',
		'        {',
		`            if (id < 0 || id >= ${tiles.length}) return new float4(0f, 0f, TileU, TileV);`,
		'            int col = id % Columns;',
		'            int row = id / Columns;',
		'            return new float4(col * TileU, row * TileV, TileU, TileV);',
		'        }',
		'    }',
		'}',
	];

	ensureDir(dirname(SOURCE_OUT));
	writeFileSync(SOURCE_OUT, lines.join('\n') + '\n');
	console.log(`Wrote ${SOURCE_OUT}`);
}

main();
