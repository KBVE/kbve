import type { BuildId } from '../config';

const COMMON_BASE: string[] = [
	'.0023333200.',
	'.0123333210.',
	'.0123444210.',
	'.0123333210.',
	'.0000000000.',
];

function build(cap: string[]): string[] {
	if (cap.length !== 7) {
		throw new Error(`Building cap must be 7 rows, got ${cap.length}`);
	}
	const grid = [...cap, ...COMMON_BASE];
	for (let i = 0; i < grid.length; i++) {
		if (grid[i].length !== 12) {
			throw new Error(`Row ${i} length ${grid[i].length} != 12`);
		}
	}
	return grid;
}

