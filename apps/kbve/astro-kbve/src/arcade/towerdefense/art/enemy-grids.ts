import type { EnemyTypeId } from '../config';

function check(grid: string[], rows: number, cols: number): string[] {
	if (grid.length !== rows) {
		throw new Error(`Enemy grid expected ${rows} rows, got ${grid.length}`);
	}
	for (let i = 0; i < rows; i++) {
		if (grid[i].length !== cols) {
			throw new Error(
				`Enemy grid row ${i} expected ${cols} cols, got ${grid[i].length}`,
			);
		}
	}
	return grid;
}

