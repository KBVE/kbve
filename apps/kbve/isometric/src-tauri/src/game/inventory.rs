// ── Conversion helpers ─────────────────────────────────────────────────
// Map game-world object types to their proto item ref.

pub fn item_from_rock_kind(kind: &RockKind) -> ItemKind {
    ItemKind::from_ref(match kind {
        RockKind::Boulder => ITEM_STONE,
        RockKind::MossyRock => ITEM_MOSSY_STONE,
        RockKind::OreCopper => ITEM_COPPER_ORE,
        RockKind::OreIron => ITEM_IRON_ORE,
        RockKind::OreCrystal => ITEM_CRYSTAL_ORE,
    })
}

pub fn item_from_flower_archetype(arch: &FlowerArchetype) -> ItemKind {
    ItemKind::from_ref(match arch {
        FlowerArchetype::Tulip => ITEM_TULIP,
        FlowerArchetype::Daisy => ITEM_DAISY,
        FlowerArchetype::Lavender => ITEM_LAVENDER,
        FlowerArchetype::Bell => ITEM_BELLFLOWER,
        FlowerArchetype::Wildflower => ITEM_WILDFLOWER,
        FlowerArchetype::Sunflower => ITEM_SUNFLOWER,
        FlowerArchetype::Rose => ITEM_ROSE,
        FlowerArchetype::Cornflower => ITEM_CORNFLOWER,
        FlowerArchetype::Allium => ITEM_ALLIUM,
        FlowerArchetype::BlueOrchid => ITEM_BLUE_ORCHID,
    })
}

pub fn item_from_mushroom_kind(kind: &MushroomKind) -> ItemKind {
    ItemKind::from_ref(match kind {
        MushroomKind::Porcini => ITEM_PORCINI,
        MushroomKind::Chanterelle => ITEM_CHANTERELLE,
        MushroomKind::FlyAgaric => ITEM_FLY_AGARIC,
    })
}
