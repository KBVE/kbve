// ── Baked itemdb ───────────────────────────────────────────────────────
// Generated by `node scripts/sync-itemdb.mjs` before each build.
// Contains all item definitions from the Astro itemdb content collection.
const BAKED_ITEMDB_JSON: &str = include_str!("../data/itemdb.json");

/// Startup system: parse the baked itemdb JSON, insert it as a Bevy resource,
/// and initialize the global reference so ProtoItemKind can resolve names/stacks.
pub fn load_baked_itemdb(mut commands: Commands) {
    match ItemDb::from_json(BAKED_ITEMDB_JSON) {
        Ok(db) => {
            let count = db.len();
            // Leak into 'static so the global OnceLock reference stays valid
            // for ProtoItemKind lookups across the entire app lifetime.
            let db_static: &'static ItemDb = Box::leak(Box::new(db));
            init_item_db(db_static);
            info!("[itemdb] loaded {count} items from baked itemdb.json");
        }
        Err(e) => {
            warn!(
                "[itemdb] failed to parse baked itemdb.json: {e:?} — inventory will use fallback names"
            );
        }
    }
    // Note: BevyItemsPlugin already inserted an empty ItemDb resource.
    // ProtoItemKind resolves via the global static ref, not the Bevy resource.
}

